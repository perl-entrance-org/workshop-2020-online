#!/usr/bin/env perl
use strict;
use warnings;

# 1. 配列 @num に好きな数値 3 つを設定しましょう。
# 2. @num の中身を print 文を使って画面に出力してみましょう。
# 3. 配列 @strings に好きな文字列(好きな何か(食べ物でもスポーツでも本でも)何でも構いません)3 つを設定しましょう。
# 4. @strings の中身を print 文を使って画面に出力してみましょう。

my @num = ( 2, 3, 5 );
print "@num" . "\n";

my @strings = ( "ramen", "yakiniku", "sushi" );
print "@strings" . "\n";
